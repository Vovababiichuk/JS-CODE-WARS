/*!SECTION

Дуже просто, за заданим числом (цілим / десятковим / обома, залежно від мови) знайти його протилежність (адитивну інверсію).

Приклади:

1: -1
14: -14
-34: 34

*/

function opposite(number) {
  return -number
}

console.log(opposite(1));
console.log(opposite(14));
console.log(opposite(-34));
