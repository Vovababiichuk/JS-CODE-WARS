/*!SECTION
Перше століття охоплює період з 1-го по 100-й рік включно, друге століття - з 101-го по 200-й рік включно і т.д.

Задача
За заданим роком повернути століття, у якому він знаходиться.
*/

function century(year) {
  return Math.ceil(year / 100);
}

